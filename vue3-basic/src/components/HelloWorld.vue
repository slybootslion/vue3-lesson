<template>
  <div class="hello">
    <h3>{{ msg }}</h3>
    <h1>count:{{ count }}</h1>
    <h1>double:{{ double }}</h1>
    <button class="btn" @click="increase">👍+1</button>
  </div>
</template>

<script lang="ts">
import { defineComponent, computed, reactive, toRefs } from "vue";

interface Data {
  count: number;
  double: number;
  increase: () => void;
}

export default defineComponent({
  name: "HelloWorld",
  props: {
    msg: String,
  },
  setup() {
    /*
    const count = ref(0)
    const double = computed(() => {
      return count.value * 2
    })

    function increase() {
      count.value++
    }

    return {
      count,
      double,
      increase
    }
    */
    const data: Data = reactive({
      count: 0,
      increase: () => data.count++,
      double: computed(() => data.count * 2),
    });
    const refData = toRefs(data);
    return {
      ...refData,
    };
  },
});
</script>

